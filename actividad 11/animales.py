class Leon:
    def __init__(self, nombre, edad, peso, color_melena):
        self.nombre = nombre
        self.edad = edad
        self.peso = peso
        self.color_melena = color_melena

    def rugir(self):
        print(f"{self.nombre} está rugiendo.")

    def cazar(self):
        print(f"{self.nombre} está cazando.")

